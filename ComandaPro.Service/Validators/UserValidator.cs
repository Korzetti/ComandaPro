using ComandaPro.Domain.Entities;
using FluentValidation;
using System.Net.Mail;

namespace ComandaPro.Service.Validators
{
    public class UserValidator : AbstractValidator<User>
    {
        public UserValidator()
        {
            RuleFor(n => n.Name)
                .NotEmpty().WithMessage("Name is needed!");
            RuleFor(d => d.Document)
                .Must(BeAValidCNPJ).When(d => d.IsRestaurant).WithMessage("Please put a valid CNPJ!");
            RuleFor(d => d.Document)
                .Must(BeAValidCPF).When(d => !d.IsRestaurant).WithMessage("Please put a valid CPF!");
            RuleFor(e => e.Email)
                .EmailAddress().WithMessage("Please put a valid Email!");
            RuleFor(a => a.Address)
                .NotEmpty().WithMessage("Address is needed!");
            RuleFor(t => t.Telephone)
                .Matches(@"^\d+$").WithMessage("Telephone must contain only numbers!")
                .Length(11).WithMessage("Please insert a valid phone number!");
            RuleFor(p => p.Password)
                .NotEmpty().WithMessage("Password cannot be empty!")
                .MaximumLength(16).WithMessage("Password length can't be bigger than 16 characters!");

        }

        private bool BeAValidCNPJ(string cnpj)
        {
            int[] multiplicador1 = new int[12] { 5, 4, 3, 2, 9, 8, 7, 6, 5, 4, 3, 2 };
            int[] multiplicador2 = new int[13] { 6, 5, 4, 3, 2, 9, 8, 7, 6, 5, 4, 3, 2 };
            int soma;
            int resto;
            string digito;
            string tempCnpj;
            cnpj = cnpj.Trim();
            cnpj = cnpj.Replace(".", "").Replace("-", "").Replace("/", "");
            if (cnpj.Length != 14)
                return false;
            tempCnpj = cnpj.Substring(0, 12);
            soma = 0;
            for (int i = 0; i < 12; i++)
                soma += int.Parse(tempCnpj[i].ToString()) * multiplicador1[i];
            resto = (soma % 11);
            if (resto < 2)
                resto = 0;
            else
                resto = 11 - resto;
            digito = resto.ToString();
            tempCnpj = tempCnpj + digito;
            soma = 0;
            for (int i = 0; i < 13; i++)
                soma += int.Parse(tempCnpj[i].ToString()) * multiplicador2[i];
            resto = (soma % 11);
            if (resto < 2)
                resto = 0;
            else
                resto = 11 - resto;
            digito = digito + resto.ToString();
            return cnpj.EndsWith(digito);
        }
        
        private bool BeAValidCPF(string cpf)
        {
            int[] multiplicador1 = new int[9] { 10, 9, 8, 7, 6, 5, 4, 3, 2 };
            int[] multiplicador2 = new int[10] { 11, 10, 9, 8, 7, 6, 5, 4, 3, 2 };
            string tempCpf;
            string digito;
            int soma;
            int resto;
            cpf = cpf.Trim();
            cpf = cpf.Replace(".", "").Replace("-", "");
            if (cpf.Length != 11)
                return false;
            tempCpf = cpf.Substring(0, 9);
            soma = 0;

            for (int i = 0; i < 9; i++)
                soma += int.Parse(tempCpf[i].ToString()) * multiplicador1[i];
            resto = soma % 11;
            if (resto < 2)
                resto = 0;
            else
                resto = 11 - resto;
            digito = resto.ToString();
            tempCpf = tempCpf + digito;
            soma = 0;
            for (int i = 0; i < 10; i++)
                soma += int.Parse(tempCpf[i].ToString()) * multiplicador2[i];
            resto = soma % 11;
            if (resto < 2)
                resto = 0;
            else
                resto = 11 - resto;
            digito = digito + resto.ToString();
            return cpf.EndsWith(digito);
        }
 
        
    }
}